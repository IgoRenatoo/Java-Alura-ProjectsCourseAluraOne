package br.com.springi.model;

public class PokemonResult {
  private String name;
  private String url;

  // Getters e Setters
  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }
}